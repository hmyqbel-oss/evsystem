export const questions: Question[] = rawQuestions.map((q, i) => ({ ...q, id: i + 1 }));

export const sections: Section[] = (() => {
  const sectionMap = new Map<string, Question[]>();
  for (const q of questions) {
    if (!sectionMap.has(q.section)) sectionMap.set(q.section, []);
    sectionMap.get(q.section)!.push(q);
  }
  return Array.from(sectionMap.entries()).map(([name, qs]) => ({ name, questions: qs }));
})();

